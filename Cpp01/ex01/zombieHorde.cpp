#include "Zombie.hpp"
#include <sstream>

Zombie*	zombieHorde( int N, std::string name )
{
	std::stringstream convIndex;
	std::string zName;

	Zombie *zHorde = new Zombie[N];
	for (int i = 0; i < N; i++)
	{
		convIndex.str("");
		convIndex.clear();
		convIndex << (i +1);
		zName = convIndex.str() + "_" + name;
		zHorde[i].setName(zName);
	}
	return (zHorde);
}
