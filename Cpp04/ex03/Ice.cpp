#include "Ice.hpp"

Ice::Ice()
{
	this->type = "ice";
}

Ice::~Ice() {}

Ice::Ice(const Ice& obj) : AMateria(obj)
{
    *this = obj;
}

Ice& Ice::operator=(const Ice& obj)
{
    if (this == &obj)
        return (*this);
    *this = obj;
    return (*this);
}

//          Amateria

AMateria* Ice::clone() const
{
    AMateria *obj = new Ice;
	return(obj);
}

void Ice::use(ICharacter& target)
{
    std::cout << "Ice: * shoots an ice bolt at " << target.getName() << " *" << std::endl;
}
